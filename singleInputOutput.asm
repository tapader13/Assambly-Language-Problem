INCLUDE 'EMU8086.INC' ;FOR NEW LINE
.MODEL SMALL
.STACK 100H 
.DATA 
.CODE

MAIN PROC 

; FOR TAKE INPUT
 MOV AH, 1 ;TAKE INPUT
 INT 21H  

; FOR NEW LINE
PRINTN "" ;PRINT NEW LINE

; FOR NEW LINE
MOV AH, 2 
MOV DL, 0DH ;CARRIAGE RETURN
INT 21H
MOV DL, 0AH ;NEW LINE
INT 21H

; FOR TAKE OUTPUT
 MOV AH, 2 ;TAKE OUTPUT
 MOV DL, AL
 INT 21H

; FOR EXIT
 MOV AH, 4CH
 INT 21H
MAIN ENDP

END MAIN