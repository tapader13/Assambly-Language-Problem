.MODEL SMALL
.STACK 100H
.DATA
CR EQU 0DH
LF EQU 0AH
MSG1 DB 'Enter a sitring:',CR,LF, '$'
MSG2 DB CR,LF,'Vowels: $'
MSG3 DB CR,LF,'Consonents: $'
MSG4 DB CR,LF,'Digits: $'
MSG5 DB CR,LF,'Spaces: $'
VOWELS DB 0
CONSONANTS DB 0
DIGITS DB 0
SPACES DB 0
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
LEA DX,MSG1
MOV AH,9
INT 21H
MOV VOWELS,0
MOV CONSONANTS,0
MOV DIGITS,0
MOV SPACES,0
MOV AH,1
NEXT:
INT 21H
CMP AL,0DH
JE EXIT
CMP AL,' '
JNE SKIP1
ADD SPACES,1
JMP NEXT
SKIP1:
CMP AL,'0'
JL NEXT
CMP AL,'9'

JG SKIP2
ADD DIGITS,1
JMP NEXT
SKIP2:
CMP AL,'A'
JL SKIP3
CMP AL,'Z'
JG SKIP3
CMP AL,'A'
JE VOWELS_INCR
CMP AL,'E'
JE VOWELS_INCR
CMP AL,'I'
JE VOWELS_INCR
CMP AL,'O'
JE VOWELS_INCR
CMP AL,'U'
JE VOWELS_INCR
ADD CONSONANTS,1
JMP NEXT
SKIP3:
CMP AL,'a'
JL NEXT
CMP AL,'z'
JG NEXT
CMP AL,'a'
JE VOWELS_INCR
CMP AL,'e'
JE VOWELS_INCR
CMP AL,'i'
JE VOWELS_INCR
CMP AL,'o'
JE VOWELS_INCR
CMP AL,'u'
JE VOWELS_INCR
ADD CONSONANTS,1
JMP NEXT
VOWELS_INCR:
ADD VOWELS,1
JMP NEXT
EXIT:
MOV AH,2
MOV DL,0DH
INT 21H
MOV DL,0AH
INT 21H

LEA DX,MSG2
MOV AH,9
INT 21H
MOV AH,2
MOV DL,VOWELS
ADD DL,30H
INT 21H
MOV DL,0DH
INT 21H
MOV DL,0AH
INT 21H
LEA DX,MSG3
MOV AH,9
INT 21H
MOV AH,2
MOV DL,CONSONANTS
ADD DL,30H
INT 21H
MOV DL,0DH
INT 21H
MOV DL,0AH
INT 21H
LEA DX,MSG4
MOV AH,9
INT 21H
MOV AH,2
MOV DL,DIGITS
ADD DL,30H
INT 21H
MOV DL,0DH
INT 21H
MOV DL,0AH
INT 21H
LEA DX,MSG5
MOV AH,9
INT 21H
MOV AH,2
MOV DL,SPACES
ADD DL,30H
INT 21H
MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN