.MODEL SMALL
.STACK 100H 
.DATA 
STRING1 DB "HELLO WORLD 1 $"
STRING2 DB "HELLO WORLD 2 $"
.CODE

MAIN PROC 

; DATA INITIALIZE
MOV AX, @DATA
MOV DS, AX

; STRING SHOW
LEA DX STRING1
MOV AH, 9
INT 21H 

; FOR NEW LINE
MOV AH, 2 
MOV DL, 0DH ;CARRIAGE RETURN
INT 21H
MOV DL, 0AH ;NEW LINE
INT 21H

; STRING SHOW
LEA DX STRING2
MOV AH, 9
INT 21H


; FOR EXIT
 MOV AH, 4CH
 INT 21H
MAIN ENDP

END MAIN