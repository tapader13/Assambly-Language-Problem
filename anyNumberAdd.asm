.MODEL SMALL
.STACK 100H
.DATA
CR EQU 0DH
LF EQU 0AH
MSG1 DB 'Enter first integer number:',CR,LF, '$'
MSG2 DB CR,LF,'Enter second integer number:',CR,LF, '$'
MSG3 DB CR,LF,'The sum is: $'
X DB 100 DUP(?)
Y DB 100 DUP(?)
Z DB 100 DUP(?)
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
LEA DX,MSG1
MOV AH,9
INT 21H
MOV SI,0
MOV AH,1
NEXT1:
INT 21H
CMP AL,0DH
JZ EXIT1
SUB AL,30H
MOV X[SI],AL
INC SI
JMP NEXT1
EXIT1:
LEA DX,MSG2
MOV AH,9
INT 21H
MOV DI,0
MOV AH,1
NEXT2:
INT 21H
CMP AL,0DH
JZ EXIT2
SUB AL,30H
MOV Y[DI],AL
INC DI

JMP NEXT2
EXIT2:
CMP SI,DI
JGE SKIP2
MOV CX,DI
JMP SKIP3
SKIP2:
MOV CX,SI
SKIP3:
MOV AL,0
MOV BL,10
MOV BP,0
NEXT3:
CMP SI,0
JGE SKIP6
CMP DI,0
JGE SKIP6
JMP EXIT3
SKIP6:
MOV DX,0
DEC SI
JL SKIP4
ADD DL,X[SI]
SKIP4:
DEC DI
JL SKIP5
ADD DL,Y[DI]
SKIP5:
ADD DL,AL
MOV AX,DX
DIV BL
MOV Z[BP],AH
INC BP
DEC CX
JG NEXT3
EXIT3:
CMP AL,0
JE SKIP7
MOV Z[BP],AL
JMP SKIP8
SKIP7:
DEC BP
SKIP8:
LEA DX,MSG3
MOV AH,9
INT 21H
MOV AH,2
MOV BX,0

NEXT4:
MOV DL,Z[BP]
ADD DL,30H
INT 21H
DEC BP
JGE NEXT4
MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN