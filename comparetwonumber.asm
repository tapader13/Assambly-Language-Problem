.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB "ENTER FIRST NUMBER: $"

MSG2 DB "ENTER SECOND NUMBER: $"
MSG3 DB "RESULT: $"

.CODE
MAIN PROC

MOV AX, @DATA
MOV DS, AX

LEA DX, MSG1
MOV AH, 9
INT 21H

MOV AH, 1
INT 21H
MOV BL, AL

LEA DX, MSG2
MOV AH, 9
INT 21H

MOV AH, 1
INT 21H
MOV CL, AL

CMP BL, CL
JG FIRST_IS_LARGER
JL SECOND_IS_LARGER

LEA DX, MSG3
MOV AH, 9
INT 21H

MOV AH, 2
MOV DL, BL
INT 21H

JMP DONE


FIRST_IS_LARGER:
LEA DX, MSG3
MOV AH, 9
INT 21H

MOV AH, 2
MOV DL, BL
INT 21H
JMP DONE

SECOND_IS_LARGER:

LEA DX, MSG3
MOV AH, 9
INT 21H

MOV AH, 2
MOV DL, CL
INT 21H
JMP DONE

DONE:

MOV AH, 4CH
INT 21H

MAIN ENDP
END MAIN
