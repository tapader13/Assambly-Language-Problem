.MODEL SMALL
.STACK 100H
.DATA
CR EQU 0DH
LF EQU 0AH
MSG1 DB 'Enter a sitring:',CR,LF, '$'
MSG2 DB CR,LF,'In reverse it is:',CR,LF, '$'
X DB 100 DUP(?)
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
LEA DX,MSG1
MOV AH,9
INT 21H
MOV SI,0
MOV AH,1
NEXT1:
INT 21H
CMP AL,0DH
JE EXIT
MOV X[SI],AL
INC SI
JMP NEXT1
EXIT:
DEC SI
LEA DX,MSG2
MOV AH,9
INT 21H
MOV AH,2
NEXT2:
MOV DL,X[SI]
INT 21H
DEC SI
JGE NEXT2
MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN