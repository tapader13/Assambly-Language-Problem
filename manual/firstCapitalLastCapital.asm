.MODEL SMALL
.STACK 100H
.DATA
CR EQU 0DH
LF EQU 0AH
MSG1 DB 'Enter a sitring for First Capital and Last Capital Letter
Searching:',CR,LF, '$'
MSG2 DB CR,LF,'The First Capital Letter is: $'
MSG3 DB CR,LF,'The Last Capital Letter is: $'
MSG4 DB CR,LF,'There is no Capital Letter in the String $'
FIRST DB '['
LAST DB '@'
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
LEA DX,MSG1
MOV AH,9
INT 21H

MOV AH,1
NEXT:
INT 21H
CMP AL,0DH
JE EXIT
CMP AL,'A'
JL NEXT
CMP AL,'Z'
JG NEXT
CMP AL,FIRST
JG SKIP2
MOV FIRST,AL
SKIP2:
CMP AL,LAST
JL SKIP3
MOV LAST,AL
SKIP3:
JMP NEXT
EXIT:
CMP FIRST,'['
JNE SKIP4
LEA DX,MSG4
MOV AH,9

INT 21H
JMP EXIT2
SKIP4:
LEA DX,MSG2
MOV AH,9
INT 21H
MOV AH,2
MOV DL,FIRST
INT 21H
LEA DX,MSG3
MOV AH,9
INT 21H
MOV AH,2
MOV DL,LAST
INT 21H
EXIT2:
MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN